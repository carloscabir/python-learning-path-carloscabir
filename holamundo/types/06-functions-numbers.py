import math

# Redondeo entero cercano
print(round(1.3))
print(round(1.7))
print(round(1.5))
# Valor Absoluto
print(abs(-1.7))
print(abs(-77))
print(abs(55))
# Redondeo al entero mas alto
print(math.ceil(1.1))
# Redondeo al entero mas bajo
print(math.floor(1.999))
# Comprobar si es nan
print(math.isnan(1.1))
# Potencia
print(math.pow(10, 3))
# Raiz cuadrada
print(math.sqrt(16))
